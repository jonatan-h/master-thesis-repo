def epp_to_eps(epp):

    if "8P" in epp:
        return "(EPS 90~110 g/l)"
    elif "9P" in epp:
        return "(EPS 83~95 g/l)"
    elif "10P" in epp:
        return "(EPS 70~83 g/l)"
    elif "11P" in epp:
        return "(EPS 60~70 g/l)"
    elif "15P" in epp:
        return "(EPS 45~55 g/l)"

    return "(EPS range unknown)"


